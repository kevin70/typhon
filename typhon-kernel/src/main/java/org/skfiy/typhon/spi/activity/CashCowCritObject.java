package org.skfiy.typhon.spi.activity;

public class CashCowCritObject {

    private int critprob;
    private int gold;
    private int cost;

    public int getCritprob() {
        return critprob;
    }

    public void setCritprob(int critprob) {
        this.critprob = critprob;
    }

    public int getGold() {
        return gold;
    }

    public void setGold(int gold) {
        this.gold = gold;
    }

    public int getCost() {
        return cost;
    }

    public void setCost(int cost) {
        this.cost = cost;
    }

}
