package org.skfiy.typhon.spi.hero;

public class ExclusiveWeapon {
    private String id;
    private String heroId;
    private int atk;
    private int matk;
    private int def;
    private int mdef;
    private int hp;
    private int atkUp;
    private int matkUp;
    private int defUp;
    private int mdefUp;
    private int hpUp;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getHeroId() {
        return heroId;
    }

    public void setHeroId(String heroId) {
        this.heroId = heroId;
    }

    public int getAtk() {
        return atk;
    }

    public void setAtk(int atk) {
        this.atk = atk;
    }

    public int getMatk() {
        return matk;
    }

    public void setMatk(int matk) {
        this.matk = matk;
    }

    public int getDef() {
        return def;
    }

    public void setDef(int def) {
        this.def = def;
    }

    public int getMdef() {
        return mdef;
    }

    public void setMdef(int mdef) {
        this.mdef = mdef;
    }

    public int getHp() {
        return hp;
    }

    public void setHp(int hp) {
        this.hp = hp;
    }

    public int getAtkUp() {
        return atkUp;
    }

    public void setAtkUp(int atkUp) {
        this.atkUp = atkUp;
    }

    public int getMatkUp() {
        return matkUp;
    }

    public void setMatkUp(int matkUp) {
        this.matkUp = matkUp;
    }

    public int getDefUp() {
        return defUp;
    }

    public void setDefUp(int defUp) {
        this.defUp = defUp;
    }

    public int getMdefUp() {
        return mdefUp;
    }

    public void setMdefUp(int mdefUp) {
        this.mdefUp = mdefUp;
    }

    public int getHpUp() {
        return hpUp;
    }

    public void setHpUp(int hpUp) {
        this.hpUp = hpUp;
    }

}
